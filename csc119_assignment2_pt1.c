/* Create a program that prompts a user for her/his address. 			            */
/* Store the user's address using the scanf() function and return a greeting     */                
/* back to the user using her/his address.                                       */                         
/*********************************************************************************/
/*                      Include a header file for printing text                  */
/*********************************************************************************/
#include <stdio.h>
/*********************************************************************************/
/*                                Declare Variables                              */
/*********************************************************************************/
char address[50]; /* accepts up to 50 characters */
/*********************************************************************************/
/*                                Main Function                                  */
/*********************************************************************************/
int main (void)
{
/*********************************************************************************/
/*                              Prompt User                                      */
/*********************************************************************************/
printf("Please enter your address in the following format: ");
printf("1234 Main St Denver, CO 80202 \n");
printf("Your Address: ");
/*********************************************************************************/
/*                                 Read/Store Input                              */
/*********************************************************************************/
scanf("%[^\n]s", address); /* allows input with spaces to be printed */
/*********************************************************************************/
/*                                 Print Results                                 */
/*********************************************************************************/
printf("Hello, you live at %s.", address);
/*********************************************************************************/
/*                                 End Program                                   */
/*********************************************************************************/
return(0);
}

